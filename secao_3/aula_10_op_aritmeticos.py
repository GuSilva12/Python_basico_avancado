# OPERADORES ARITMETICOS
print('Operadores Aritméticos: ')

# Adicao
adicao = 10 + 10
print("Adição: ", adicao)

# Subtracao
subtracao = 10 - 5
print('Subtração: ', subtracao)

# Multiplicacao
multiplicacao = 10 * 10
print("Multiplicação: ", multiplicacao)

# Divisao
divisao = 10 / 2.2
print('Divisão: ', divisao)

# Divisao inteira
diviao_inteira = 10 // 2.2
print("Divisão Inteira: ", diviao_inteira)

# Exponenciacao
exponenciacao = 10 ** 10
print('Exponenciação: ', exponenciacao)

# Modulo
modulo = 55 % 2
print("Módulo: ", modulo)