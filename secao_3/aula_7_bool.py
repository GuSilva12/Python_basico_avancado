# TIPO BOOLEANO (BOOL)

# Ao questionar algo no programa, so ha duas respostas possiveis: True (verdadeiro) ou False (falso)

# para se questionar, podem ser usados valores logicos:

# > - maior
print(10>11)
print(1.1>2.2)
print()

# < - menor
print(10<11)
print(1.1<2.2)
print()

# == - igual (idÃªntico)
print(10==11)
print(1.1>2.2)
print()

# >= - maior ou igual
print(10>=11)
print(1.1>=2.2)
print()

# <= - menor ou igual
print(10<=11)
print(1.1<=2.2)
