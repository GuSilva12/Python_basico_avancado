# O TIPO STRING

"""
PYTHON = linguagem de programação

TIPAGEM = Dinamica e Forte (nao exige que o tipo do valor seja explicitamente declarado)

str = string = texto

STRINGS =  textos dentro de aspas
"""

# Numero inteiro
print("imprimindo um valor inteiro")
print("Valor inteiro impresso sem a necessidade de aspas, preservando seu tipo (int, que não precisa ser declarado)")
print(1234)             # Valor inteiro impresso sem a necessidade de aspas, preservando seu tipo (int, que não precisa ser declarado)
print("\n")
print("\n")



# Aspas simples
print("imprimindo uma string com aspas simples")
print('Oficina G3')     # String impressa usando aspas simples
print("\n")
print("\n")



# Aspas duplas
print("imprimindo uma string com aspas duplas")
print("Oficina G3")     # String impressa usando aspas duplas
print("\n")
print("\n")



# Escape "\" (permite a inclusao de caracteres especiais em strings)

print("Caracter de escape - (\)")      # Imprimindo uma mensagem na tela
print("permite a inclusao de caracteres especiais em strings")      # Imprimindo uma mensagem na tela
print('Oficina \'G3\'')     # imprimindo uma string com o mesmo tipo de aspa de sua declaracao(''), permitida atraves do caracter de escape (\)
print("Oficina \"G3\"")     # imprimindo uma string com o mesmo tipo de aspa de sua declaracao(""), permitida atraves do caracter de escape (\)
print("\n")
print("\n")


# r (permite exibir o caracter de escape)
print("Exibindo o caracter de escape (r)")
print("permite exibir o caracter de escape")
print("Caracter de escape - (\)")      # Imprimindo uma mensagem na tela
print("permite a inclusao de caracteres especiais em strings")      # Imprimindo uma mensagem na tela
print(r'Oficina \'G3\'')     
print(r"Oficina \"G3\"")
print("\n")
print("\n")
