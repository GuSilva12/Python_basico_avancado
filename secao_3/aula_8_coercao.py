# COERCAO DE TIPOS (type conversion, typecasting, coercion)

# Conversão dos tipos dos argumentos (variaveis)
# TIPOS IMUTAVEIS E PRIMITIVOS: str, int, float, bool

# IMUTAVEL: uma vez criada uma variavel de certo tipo, este não pode ser alterado sem haver uma coerção

# convertendo um 'int' em 'float'
print(float(1))
print()

# convertendo um float em int
print(int(1.0))
print()

# convertendo um int em str
print(str(1))
print()

# convertendo um float em str
print(str(1.0))
print()

# convertendo uma str em int
print(int('1'))
print(int("1"))
print()

# convertendo uma str em float
print(float('1'))
print(float("1"))
print()

# convertendo um int para str para concatenar com outra str
print(str(11) + 'b')

# convertendo um float para str para concatenar com outra str
print(str(1.1) + 'b')
print()

# Convertendo uma str para int para somar com um int
print(int('11') + 2)
print()

# Convertendo uma str para float para somar com um int
print(float('11') + 2.0)
