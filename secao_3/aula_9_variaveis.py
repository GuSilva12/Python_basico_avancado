# VARIAVEIS

# usadas para salvar algo na memoria do computador

# PEP 8: inicie variaveis com letras minusculas, podendo usar numeros e underline
# Sinal de igualdade (=) usado como operador de atribuicao de valores a uma variavel 

# EXEMPLO: nome_variavel = <expessao>

nome_completo = 'Acer Nitro 5'                  # str
soma_dois_mais_dois = 2+2                       # int
idade = 30                                      # int
maior_de_idade = idade >= 18                    # bool (retorna True)
menor_de_idade = idade <= 18                    # bool (retorna False)


print(nome_completo)                            # Acer Nitro 5
print(soma_dois_mais_dois)                      # 4
print(nome_completo, soma_dois_mais_dois)       # Acer Nitro 5 4
print(maior_de_idade)                           # True
print(menor_de_idade)                           # False
print('Nome: ', nome_completo, "\nidade: ", idade, "\nmaior de idade: ", maior_de_idade, "\nmenor de idade: ", menor_de_idade)