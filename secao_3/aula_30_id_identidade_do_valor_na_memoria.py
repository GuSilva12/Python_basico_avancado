# ID - A IDENTIDADE DO VALOR NA MEMÓRIA 

v1 = 'a'        # armazenando um caracter em uma variável
v2 = 'a'        # armazenando um caracter em uma variável
print(id(v1))   # verificando a identidade de um elemento na memória, nesse caso, no elemento armazenado na variável 'v1'
print(id(v2))   # verificando a identidade de um elemento na memória, nesse caso, no elemento armazenado na variável 'v2'

# note que, uma vez que os elementos, apesar de armazenados em variáveis diferentes, são iguais, a id aponta para o mesmo enrredeço de memória